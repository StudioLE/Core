namespace StudioLE.Extensions.Logging;

/// <summary>
/// ANSI color for console output.
/// </summary>
public enum AnsiColor
{
    /// <summary>
    /// Black, or gray if bright.
    /// </summary>
    Black = 0,

    /// <summary>
    /// Red
    /// </summary>
    Red = 1,

    /// <summary>
    /// Green
    /// </summary>
    Green = 2,

    /// <summary>
    /// Yellow
    /// </summary>
    Yellow = 3,

    /// <summary>
    /// Blue
    /// </summary>
    Blue = 4,

    /// <summary>
    /// Magenta
    /// </summary>
    Magenta = 5,

    /// <summary>
    /// Cyan
    /// </summary>
    Cyan = 6,

    /// <summary>
    /// White
    /// </summary>
    White = 7,

    /// <summary>
    /// The default color.
    /// </summary>
    Default = 9
}
